// Написати програм који исписује дијамант са рупом за унето N:
// (Нема потребе проверавати улазне податке)
// Напомена: За пола поена, исписати горњи део слике
// (првих N редова, тј до реда који садржи празнину, укључујући њега)
{
    let N = 5

    for(let x = 1; x <= N; x++){
        let red = ' '
        for(let y = 1; y <= x; y += 2){
            red = ' '.repeat(N - x) + '*'.repeat(y)
        }
        console.log(red)
    }
}